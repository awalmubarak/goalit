package com.anditer.goalit.POJOs;

/**
 * Task objects
 */

public class Task {
    String title;
    Boolean status;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public Boolean getStatus() {
        return status;
    }

    public void setStatus(Boolean status) {
        this.status = status;
    }
}
